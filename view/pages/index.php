      <div class="text">
        <p>
          Chiro Popov bestaat al sinds 1946.<br>
          Toen werd de Chiro ‘Herman Wijns’ opgericht in Gijzegem voor jongens. Enkele jaren later werd er ook een Chiro voor meisjes opgericht, ‘Sint Martinus’. De namen veranderden al snel in Chiro Popov en Chiro Jopa.<br>
          In 1992 werden deze twee Chirowerkingen verenigd onder de gemengde groep Chiro Popov en zo bestaan we nu nog steeds!<br>
          <a class="link" href="https://chiro.be/statuut-van-de-vrijwilliger">Chiro Popov steunt hoofdzakelijk op vrijwilligers.</a>
        </p>

        <p>
          Iedereen <span class="bold">tussen 6 en 18 jaar</span> is welkom op de chiro. Onze activiteiten vinden plaats vanaf de tweede zondag van september tot de laatste zondag van juni. Voor de jongste afdelingen is het <span class="bold">Chiro van 14u tot 17u</span>.
          <span class="bold">Vanaf Tito is het Chiro van 14u tot 18u.</span>
          De leiding zal op tijd verwittigen wanneer de uren of dagen van de activiteiten afwijken van de normale werking.
        </p>

        <p>
          Wij vragen aan het begin van het jaar <span class="bold">€25 inschrijvingsgeld</span>. Voor dit geld kan een lid een heel jaar lang de activiteiten komen meevolgen. In dit bedrag zit ook een Chiroverzekering inbegrepen.
          Verder vragen wij om <span class="bold">elke week €0,50</span> mee te geven voor een drankje.
          De oudsten gaan vaak wel eens samen eten of frietjes halen, hoeveel dit kost wordt afgesproken met de afdelingsleiding. Niemand wordt hier toe verplicht!
        </p>
